"""
2 - Crie uma função que recebe 3 números como parâmetros e exiba a soma entre 
eles.
"""


def summ(n1, n2, n3):
    print(n1 + n2 + n3)


number1 = int(input('Digite um número: '))
number2 = int(input('Digite outro número: '))
number3 = int(input('Digite mais um número: '))

summ(number1, number2, number3)
